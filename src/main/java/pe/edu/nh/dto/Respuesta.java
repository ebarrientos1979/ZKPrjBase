package pe.edu.nh.dto;

public class Respuesta {
	private int valor;
	private String mensaje;
	
	public void setValor(int valor) {
		this.valor = valor;
	}
	
	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}
	
	public int getValor() {
		return this.valor;
	}
	
	public String getMensaje() {
		return this.mensaje;
	}

}
